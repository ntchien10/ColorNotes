package com.example.xeviet.ui.home;

public class LoaiXe {
    int IconXe;
    String TenXe;

    public LoaiXe() {
    }

    public LoaiXe(int iconXe, String tenXe) {
        IconXe = iconXe;
        TenXe = tenXe;
    }

    public int getIconXe() {
        return IconXe;
    }

    public void setIconXe(int iconXe) {
        IconXe = iconXe;
    }

    public String getTenXe() {
        return TenXe;
    }

    public void setTenXe(String tenXe) {
        TenXe = tenXe;
    }
}
